"use client";

import { useState } from "react";
import useSWR from "swr";
import { apiPost, fetcher } from "@/lib/api";
import type { EvaluationRunSummary } from "@/types/api";

function pct(x: number | undefined): string {
  return x == null ? "—" : `${(x * 100).toFixed(1)}%`;
}

export default function EvaluationPage() {
  const { data, mutate } = useSWR<EvaluationRunSummary[]>("/api/v1/evaluations", fetcher);
  const [busy, setBusy] = useState(false);

  async function runEval() {
    setBusy(true);
    try {
      await apiPost("/api/v1/evaluations/run?per_type=1&small=true");
      await mutate();
    } finally {
      setBusy(false);
    }
  }

  const latest = data?.[0];
  const m = latest?.metrics ?? null;
  const byType: Record<string, { n: number; top1_accuracy: number }> =
    m?.by_incident_type ?? {};

  return (
    <div>
      <h1>Evaluation Dashboard</h1>
      <p className="muted">
        Metrics are generated by the evaluation runner — never hand-written. Each incident is
        evaluated in isolation against a freshly restored clean baseline.
      </p>

      <div className="card" style={{ marginBottom: "1rem" }}>
        <button onClick={runEval} disabled={busy}>
          {busy ? "Running evaluation…" : "Run evaluation (1 per category)"}
        </button>
      </div>

      {!m && <div className="card muted">No evaluation runs yet. Run one above.</div>}

      {m && (
        <>
          <div className="grid" style={{ marginBottom: "1rem" }}>
            <div className="card">
              <div className="muted">Root-cause top-1</div>
              <div className="metric">{pct(m.root_cause_top1_accuracy)}</div>
            </div>
            <div className="card">
              <div className="muted">Root-cause top-3</div>
              <div className="metric">{pct(m.root_cause_top3_accuracy)}</div>
            </div>
            <div className="card">
              <div className="muted">Classification macro-F1</div>
              <div className="metric">{m.classification_macro_f1?.toFixed(3)}</div>
            </div>
            <div className="card">
              <div className="muted">Unsupported (report)</div>
              <div className="metric">{pct(m.unsupported_diagnosis_rate_report_level)}</div>
            </div>
            <div className="card">
              <div className="muted">Escalation F1</div>
              <div className="metric">{pct(m.escalation_f1)}</div>
            </div>
            <div className="card">
              <div className="muted">Mean latency</div>
              <div className="metric">{m.mean_latency_seconds?.toFixed(3)}s</div>
            </div>
            <div className="card">
              <div className="muted">Brier score</div>
              <div className="metric">{m.calibration?.brier_score?.toFixed(3)}</div>
            </div>
            <div className="card">
              <div className="muted">Est. cost (USD)</div>
              <div className="metric">{m.total_estimated_cost?.toFixed(4)}</div>
            </div>
          </div>

          <div className="card">
            <h3>Top-1 accuracy by incident type</h3>
            <table>
              <thead>
                <tr>
                  <th>Incident type</th>
                  <th>N</th>
                  <th>Top-1</th>
                </tr>
              </thead>
              <tbody>
                {Object.entries(byType).map(([t, s]) => (
                  <tr key={t}>
                    <td>{t}</td>
                    <td>{s.n}</td>
                    <td>{pct(s.top1_accuracy)}</td>
                  </tr>
                ))}
              </tbody>
            </table>
            <p className="muted" style={{ marginTop: "0.5rem" }}>
              Run {latest?.evaluation_run_id} · model {latest?.model_name}
            </p>
          </div>
        </>
      )}
    </div>
  );
}
